# all data types example

# String (str)
name = "Rimel"
print("String:", name)

# Integer (int)
age = 29
print("Integer:", age)

# Float (float)
height = 5.8
print("Float:", height)

# Boolean (bool)
is_sqa = True
print("Boolean:", is_sqa)

# List
fruits = ["Apple", "Banana", "Mango"]
print("List:", fruits)

# Tuple
colors = ("Red", "Green", "Blue")
print("Tuple:", colors)

# Set
numbers = {1, 2, 3, 4, 5}
print("Set:", numbers)

# Dictionary
person = {
    "name": "Rimel",
    "age": 29,
    "profession": "SQA Engineer"
}
print("Dictionary:", person)

# NoneType
data = None
print("NoneType:", data)

# Complex
complex_num = 2 + 3j
print("Complex:", complex_num)

# Type Checking
print("\nType Checking:")
